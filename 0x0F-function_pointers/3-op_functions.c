/**
 * op_add - function that adds two variables given
 *
 * @a: value from main
 * @b: value from main
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that subtracts two variables given
 *
 * @a: value from main
 * @b: value from main
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that multiplies two variables given
 *
 * @a: value from main
 * @b: value from main
 *
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that divides two variables given
 *
 * @a: value from main
 * @b: value from main
 *
 * Return: quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that modulos two variables given
 *
 * @a: value from main
 * @b: value from main
 *
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
